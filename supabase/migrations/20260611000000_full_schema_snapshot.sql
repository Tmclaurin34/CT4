CREATE OR REPLACE FUNCTION public.clicktide_debit_gift_wallet(client_id uuid, debit_amount numeric, description text, reference text DEFAULT NULL::text)
 RETURNS numeric
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  current_balance numeric;
  new_balance numeric;
begin
  if client_id is null then
    raise exception 'Client id is required';
  end if;

  if debit_amount is null or debit_amount <= 0 then
    raise exception 'Debit amount must be greater than zero';
  end if;

  if auth.uid() is null then
    raise exception 'Not allowed to debit this wallet';
  end if;

  if auth.uid() is distinct from client_id and not public.clicktide_can_manage_fulfillment(client_id) then
    raise exception 'Not allowed to debit this wallet';
  end if;

  select public.clicktide_wallet_balance(client_id) into current_balance;

  if current_balance < debit_amount then
    raise exception 'Insufficient gift wallet balance';
  end if;

  new_balance := current_balance - debit_amount;

  insert into public.wallet (
    user_id,
    "desc",
    amount,
    balance,
    transaction_type,
    status,
    reference_id
  ) values (
    client_id,
    coalesce(nullif(description, ''), 'Gift sent'),
    -debit_amount,
    new_balance,
    'gift_debit',
    'posted',
    reference
  );

  return new_balance;
end;
$function$
;

