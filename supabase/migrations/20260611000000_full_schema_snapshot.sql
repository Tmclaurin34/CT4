CREATE OR REPLACE FUNCTION public.enforce_customer_cap()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
declare
  p text;
  cap integer;
  n integer;
begin
  if NEW.user_id is null then
    return NEW; -- legacy/demo rows
  end if;
  select lower(coalesce(plan,'')) into p from clicktide where user_id = NEW.user_id limit 1;
  cap := case p
    when 'local' then 200
    when 'growth' then 1000
    when 'scale' then null
    else 200
  end;
  if cap is not null then
    select count(*) into n from customers where user_id = NEW.user_id;
    if n >= cap then
      raise exception 'PLAN_CUSTOMER_LIMIT_REACHED: your plan allows % customers. Upgrade to add more.', cap;
    end if;
  end if;
  return NEW;
end;
$function$
;

